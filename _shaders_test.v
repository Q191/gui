fn read_generated_shader_header() string {
	return os.read_file(generated_shader_header_path()) or { panic(err) }
}

fn read_gui_source(name string) string {
	return os.read_file(os.join_path(os.dir(@FILE), name)) or { panic(err) }
}

fn count_occurrences(haystack string, needle string) int {
	mut count := 0
	mut offset := 0
	for {
		index := haystack.index_after(needle, offset) or { break }
		count++
		offset = index + needle.len
	}
	return count
}

fn normalize_whitespace(src string) string {
	return src.fields().join(' ')
}

fn sanitize_generated_shader_header(src string) string {
	mut lines := []string{}
	for line in src.split_into_lines() {
		if line.contains('sokol-shdc --input') {
			lines << '        ${generated_shader_cmdline()}'
			continue
		}
		lines << line
	}
	return lines.join('\n')
}

