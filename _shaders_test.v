fn test_d3d11_custom_shader_guard_returns_before_runtime_source_pipeline() {
	source := read_gui_source('shaders.v')
	guard_index := source.index('custom GLSL/Metal shaders are not supported with -d sokol_d3d11') or {
		panic(err)
	}
	glsl_build_index := source.index('fs_source := build_glsl_fragment(shader.glsl)') or {
		panic(err)
	}
	make_shader_index := source.index('shader: gfx.make_shader(&shader_desc)') or { panic(err) }
	guard_return_index := source.index_after('return pip', guard_index) or { panic(err) }
	assert guard_index < glsl_build_index
	assert guard_index < make_shader_index
	assert guard_return_index < glsl_build_index
	assert guard_return_index < make_shader_index
}

fn test_generated_builtin_shader_header_is_fresh_when_requested() {
	if os.getenv('GUI_CHECK_GENERATED_SHADERS') != '1' {
		return
	}

	tmp_dir := os.join_path(os.temp_dir(), 'gui_builtin_shader_fresh_${os.getpid()}')
	tmp_shader_dir := os.join_path(tmp_dir, 'shaders')
	os.rmdir_all(tmp_dir) or {}
	os.mkdir_all(tmp_shader_dir) or { panic(err) }
	tmp_source := os.join_path(tmp_shader_dir, 'gui_builtin.glsl')
	tmp_header := os.join_path(tmp_shader_dir, 'gui_builtin.h')
	os.cp(generated_shader_source_path(), tmp_source) or { panic(err) }

	cmd := '${os.quoted_path(@VEXE)} shader -l glsl410 -l hlsl4 -l metal_macos ${os.quoted_path(tmp_source)}'
	res := os.execute(cmd)
	assert res.exit_code == 0, res.output

	checked_in := sanitize_generated_shader_header(read_generated_shader_header())
	regenerated := sanitize_generated_shader_header(os.read_file(tmp_header) or { panic(err) })
	assert checked_in == regenerated, 'generated shader header is stale; run `v shader -l glsl410 -l hlsl4 -l metal_macos shaders/gui_builtin.glsl`'
	os.rmdir_all(tmp_dir) or {}
}

fn test_pack_shader_params() {
	// 10.0 px radius -> 40 fixed-point units.
	mut val := pack_shader_params(10.0, 0.0)
	assert val == 163840.0

	// 5.5 px radius, 2.0 px thickness.
	val = pack_shader_params(5.5, 2.0)
	assert val == 90120.0

	// 0 px radius, 10 px thickness.
	val = pack_shader_params(0.0, 10.0)
	assert val == 40.0

	// Reconstruction path mirrors shader decode.
	val = pack_shader_params(25.0, 15.5)
	unpacked_radius := math.floor(val / 4096.0) / 4.0
	unpacked_thickness := math.fmod(val, 4096.0) / 4.0

	assert unpacked_radius == 25.0
	assert unpacked_thickness == 15.5
}

fn test_pack_shader_params_clamps_to_max() {
	val := pack_shader_params(5000.0, 5000.0)
	assert val == 16777215.0
	unpacked_radius := math.floor(val / 4096.0) / 4.0
	unpacked_thickness := math.fmod(val, 4096.0) / 4.0
	assert unpacked_radius == 1023.75
	assert unpacked_thickness == 1023.75
}

