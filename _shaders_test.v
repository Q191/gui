fn test_generated_builtin_shader_header_has_d3d11_descriptor_coverage() {
	header := read_generated_shader_header()
	program_count := generated_shader_programs().len
	assert header.contains('SG_BACKEND_GLCORE')
	assert header.contains('SG_BACKEND_METAL_MACOS')
	assert count_occurrences(header, 'if (backend == SG_BACKEND_D3D11)') == program_count
	assert count_occurrences(header, 'desc.vs.d3d11_target = "vs_4_0";') == program_count
	assert count_occurrences(header, 'desc.fs.d3d11_target = "ps_4_0";') == program_count
	assert header.contains('gui_rect_vs_source_hlsl4')
	assert header.contains('gui_shadow_vs_source_hlsl4')
	assert header.contains('gui_gradient_vs_source_hlsl4')
	assert header.contains('gui_filter_vs_source_hlsl4')
	assert header.contains('gui_filter_sgl_vs_source_hlsl4')
	assert header.contains('gui_filter_texture_fs_source_hlsl4')
}

fn test_generated_builtin_shader_header_uses_current_glcore_target() {
	header := read_generated_shader_header()
	generated_docs := read_gui_source('shaders/README.md')
	shader_docs := read_gui_source('docs/SHADERS.md')
	normalized_generated_docs := normalize_whitespace(generated_docs)
	normalized_shader_docs := normalize_whitespace(shader_docs)

	assert header.contains('sokol-shdc --input shaders/gui_builtin.glsl --output shaders/gui_builtin.h --slang glsl410:hlsl4:metal_macos')
	assert header.contains('#version 410')
	assert header.contains('gui_rect_vs_source_glsl410')
	assert header.contains('gui_rounded_rect_fs_source_glsl410')
	assert !header.contains('glsl330')
	assert !header.contains('#version 330')

	assert generated_docs.contains('`glsl410` target is intentional')
	assert normalized_generated_docs.contains('Do not rewrite the generated header to `glsl330`')
	assert normalized_generated_docs.contains('runtime custom `Shader.glsl` body format')

	assert shader_docs.contains('Generated built-in shaders are separate from this runtime `Shader` API')
	assert shader_docs.contains('`glsl410` for desktop `SG_BACKEND_GLCORE`')
	assert normalized_shader_docs.contains('GLSL 3.3 contract below applies only to runtime custom `Shader.glsl` bodies')
}

